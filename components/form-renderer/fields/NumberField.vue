<template>
  <u-form-item
      :label="field.translations[lang]?.label"
      :prop="field.field_key"
      :required="field.is_required === 1"
      :label-width="labelWidth"
  >
    <u-number-box
        v-model="value"
        :min="field.field_options?.min"
        :max="field.field_options?.max"
        :step="field.field_options?.step || 1"
        :disabled="disabled"
        :input-width="200"
        :button-size="50"
        :decimal-length="field.field_options?.precision"
    />
    <text class="field-help-text" v-if="field.translations[lang]?.help_text">
      {{ field.translations[lang].help_text }}
    </text>
  </u-form-item>
</template>

<script>
export default {
  name: 'NumberField',
  props: {
    field: Object,
    modelValue: Number,
    lang: String,
    disabled: Boolean,
    formValues: Object,
    formRef: Object,
    labelWidth: {
      type: [String, Number],
      default: '160'
    }
  },
  emits: ['update:modelValue'],
  computed: {
    value: {
      get() {
        return this.modelValue
      },
      set(val) {
        this.$emit('update:modelValue', val)
      }
    }
  }
}
</script>

